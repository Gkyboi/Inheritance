public class SystemProgrammer extends Employee {
    
    int bonus;
    
    public SystemProgrammer(float salary, int bonus) {
        super(salary);
        this.bonus = bonus;
    }

    public int getBonus() {
        return bonus;
    }

    public void setBonus(int bonus) {
        this.bonus = bonus;
    }

    public float getSalary() {
        return salary;
    }

    public void setSalary(float salary) {
        this.salary = salary;
    }
    
    
}
